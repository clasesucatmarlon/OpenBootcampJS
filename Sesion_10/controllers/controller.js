/**
 * Function for sum two values
 * @param {a} First value 
 * @param {b} Second value
 * @returns sum between value a and value b
 */
export function add (a, b) {
    return a + b;
};

/**
 * Function for calculated the product of two values
 * @param {a} First value 
 * @param {b} Second value
 * @returns product between value a and value b
 */
export function product (a, b) {
    return a * b;
};